Room Graph_loadRoom(int /*roomIdx*/, IGame* /*game*/)
{
  Room r;

  r.name = "test room";

  auto onCell =
    [&] (int x, int y, int z, int /*tile*/)
    {
      r.tiles.set(x, y, z, getTile(Vector3i(x, y, z)));

      if(rand() % 123 == 0)
        r.tiles.set(x, y, z, 1);
    };

  r.tiles.resize(Size3i(64, 64, 16));
  r.tiles.scan(onCell);

  r.start = Vector3i(4, 4, 4);
  r.theme = 2;

  return r;
}

